export const FW = 5;
export const FH = 7;

function glyph(ch: string): string[] { return G[ch] || G[ch.toUpperCase()] || G["?"]; }

export function textWidth(str: string, scale = 1, tracking = 1): number {
  return str.length * (FW + tracking) * scale - tracking * scale;
}

function putPx(buf: PixBuf, x: number, y: number, rgb: RGB): void {
  x |= 0; y |= 0;
  if (x < 0 || y < 0 || x >= buf.w || y >= buf.h) return;
  const i = (y * buf.w + x) * 4;
  buf.data[i] = rgb[0]; buf.data[i + 1] = rgb[1]; buf.data[i + 2] = rgb[2]; buf.data[i + 3] = 255;
}

/** Draw `str` into the RGBA buffer at (x,y). Optional 1px drop `shadow`. */
export function drawText(buf: PixBuf, str: string, x: number, y: number, rgb: RGB, scale = 1, tracking = 1, shadow?: RGB | null): void {
  let cx = x;
  for (const ch of str) {
    const g = glyph(ch);
    for (let ry = 0; ry < FH; ry++)
      for (let rx = 0; rx < FW; rx++)
        if (g[ry][rx] === "1")
          for (let sy = 0; sy < scale; sy++)
            for (let sx = 0; sx < scale; sx++) {
              const px = cx + rx * scale + sx, py = y + ry * scale + sy;
              if (shadow) putPx(buf, px + scale, py + scale, shadow);
              putPx(buf, px, py, rgb);
            }
    cx += (FW + tracking) * scale;
  }
}
